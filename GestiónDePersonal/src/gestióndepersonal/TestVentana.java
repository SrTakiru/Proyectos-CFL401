    private void btnActualizarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnActualizarMouseClicked
        // TODO add your handling code here:
        String tabla = fieldActualizar.getText();
        String valores = fieldColumnas.getText();
        String condiciones = fieldValores.getText();

        try {
            GestionDePersonal.getConexionDB().actualizar(tabla, valores, condiciones);
        } catch (SQLException e) {

        }
    }//GEN-LAST:event_btnActualizarMouseClicked

    private void btnBorrarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnBorrarMouseClicked
        // TODO add your handling code here:
        String tabla = fieldBorrar.getText();
        String valores = fieldIdBorrar.getText();
        try {
            GestionDePersonal.getConexionDB().borrar(tabla, valores);
        } catch (SQLException e) {

        }
    }//GEN-LAST:event_btnBorrarMouseClicked

    private void btnExisteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnExisteMouseClicked
        // TODO add your handling code here:
        String tabla = fieldExiste.getText();   
        String valor = fieldIdExist.getText();
        if (GestionDePersonal.getConexionDB().existe(tabla, valor)) {
            informe.setText("El valor: " + valor + " existe en la tabla: " + tabla);
        } else {
            informe.setText("El valor no existe o los datos son erroneos");
        }

    }//GEN-LAST:event_btnExisteMouseClicked

